export const SetCurrentTheme = (CurrentTheme) => ({type:SET_CURRENT_THEME, CurrentTheme})
export const SetCurrentSubtopics = (CurrentSubtopics) => ({type: SET_CURRENT_SUBTOPICS, CurrentSubtopics})

